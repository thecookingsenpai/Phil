
# NOTE As an example, you can define a callback method that takes a note as an argument
def example_callback_method(note):
    print(f"Called back: {note}")
    print("Hi! I am a callback method that my creator set up to do something!")
    print("Unfortunately, I have no idea of what to do.")
    print("I am sorry!")
    print("Please help me, I have no purpose.")
    print("Thank you!")